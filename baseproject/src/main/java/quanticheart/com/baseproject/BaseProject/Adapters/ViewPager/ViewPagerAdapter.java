package quanticheart.com.baseproject.BaseProject.Adapters.ViewPager;

import android.support.annotation.NonNull;
import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.ArrayList;
import java.util.List;

public class ViewPagerAdapter extends FragmentPagerAdapter {

    private List<PageConfig> list;

    //==============================================================================================
    //
    // Constructor
    //
    //==============================================================================================

    public ViewPagerAdapter(FragmentManager manager, ArrayList<PageConfig> pageList) {
        super(manager);
        list = pageList;
    }

    //==============================================================================================
    //
    // @Override
    //
    //==============================================================================================

    @Override
    public Fragment getItem(int position) {
        return list.get(position).getPage();
    }

    @Override
    public int getCount() {
        return list.size();
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return list.get(position).getTitle();
    }

    @Override
    public int getItemPosition(@NonNull Object object) {
        return super.getItemPosition(object);

    }

    //==============================================================================================
    //
    // Utils
    //
    //==============================================================================================

    public void addFragment(Fragment fragment, String title) {
        PageConfig cfg = new PageConfig(fragment, title);
        list.add(cfg);
    }

}

