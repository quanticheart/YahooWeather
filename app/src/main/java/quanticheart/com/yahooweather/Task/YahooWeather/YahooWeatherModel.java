package quanticheart.com.yahooweather.Task.YahooWeather;

import android.util.Log;

import java.io.Serializable;
import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

import quanticheart.com.yahooweather.BuildConfig;

public class YahooWeatherModel implements Serializable
{

    @SerializedName("query")
    @Expose
    private Query query;
    private final static long serialVersionUID = -960440821898809585L;

    /**
     * No args constructor for use in serialization
     * 
     */
    public YahooWeatherModel() {
    }

    /**
     * 
     * @param query
     */
    public YahooWeatherModel(Query query) {
        super();
        this.query = query;
    }

    public Query getQuery() {
        return query;
    }

    public void setQuery(Query query) {
        this.query = query;
    }

    //==============================================================================================
    //
    // Utils
    //
    //==============================================================================================

    public static void msgOnly(String msg) {
        if (debugStatus()) {
            Log.w("Notice", msg);
        }
    }

    public static void msgErro(String msg) {
        if (debugStatus()) {
            Log.w("Error", msg);
        }
    }

    private static boolean debugStatus() {
        return BuildConfig.DEBUG;
    }

}
